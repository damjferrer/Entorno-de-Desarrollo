/*Funcions*/

int suma (int op1, int op2){
    return (op1+op2);
}

int resta (int op1, int op2){
    return (op1-op2);
}

int multiplica (int op1, int op2){
    return (op1*op2);
}

int divideix (int op1, int op2){
    return (op1/op2);
}

int major(int num1,int num2){
    int resultat;
    if(num1>num2)
        resultat=num1;
    else
        resultat=num2;

return resultat;
}